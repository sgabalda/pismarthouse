package cat.casadepalla.casaEspavilada.sunnyBoy.web;

/**
 *
 * @author gabalca
 */
public class SunnyBoyAdapterException extends Exception {

    public SunnyBoyAdapterException(String message) {
        super(message);
    }

    public SunnyBoyAdapterException(String message, Throwable cause) {
        super(message, cause);
    }

    public SunnyBoyAdapterException(Throwable cause) {
        super(cause);
    }

    public SunnyBoyAdapterException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
    
}
